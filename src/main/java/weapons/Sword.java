package weapons;

public class Sword {

    private int swordDamageValue;

    public Sword(int swordDamageValue){
        this.swordDamageValue = swordDamageValue;
    }

    public int getSwordDamageValue() {
        return swordDamageValue;
    }

    public void setSwordDamageValue(int swordDamageValue) {
        this.swordDamageValue = swordDamageValue;
    }
}
