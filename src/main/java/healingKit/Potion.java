package healingKit;

public class Potion {

    private int potionHealValue;

    public Potion(int potionHealValue){
        this.potionHealValue = potionHealValue;
    }

    public int getPotionHealValue() {
        return potionHealValue;
    }

    public void setPotionHealValue(int potionHealValue) {
        this.potionHealValue = potionHealValue;
    }


}
